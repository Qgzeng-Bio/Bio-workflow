    if source_type == "conda":
        argv = build_conda_argv(target_env, package_name, args.version or "", channels, args.reuse_existing)
        install_method = "conda_install_existing_env" if args.reuse_existing else "conda_create_env"
        commands = [shlex.join(argv)]
        command_argv = [argv]
        expected_writes = [
            f"Conda environment named {target_env}",
            "Conda/Mamba package cache in the user's configured Conda directories",
        ]
        risk_notes = [
            "Requires network access unless packages are already cached.",
            "Mutates a Conda environment; rollback may require removing or repairing the env.",
            "If the env appears before execution and reuse_existing is false, install will block.",
        ]
        network_required = True
    else:
        install_method = f"{source_type}_proposal_only"
        commands = []
        command_argv = []
        expected_writes = ["No automatic writes are defined by third-version onboarding."]
        risk_notes = [
            "This source type is proposal-only in program_onboard.py.",
            "Review official installation instructions and confirm target paths before any manual action.",
            "Do not write under ~/tools/ without separate confirmation.",
        ]
        network_required = source_type in {"container", "github", "source", "binary"}

    proposal = {
        "proposal_schema_version": SCHEMA_VERSION,
        "created_by": CREATED_BY,
        "program_key": key,
        "program_name": args.program,
        "package_name": package_name,
        "version_spec": args.version or "",
        "source_type": source_type,
        "install_method": install_method,
        "target_env": target_env,
        "channels": channels,
        "commands": commands,
        "command_argv": command_argv,
        "expected_writes": expected_writes,
        "network_required": network_required,
        "risk_notes": risk_notes,
        "reuse_existing": bool(args.reuse_existing),
        "created_at": iso_now(),
        "evidence_dir": str(bundle),
    }
    write_text(bundle / "install_proposal.json", json.dumps(proposal, indent=2, ensure_ascii=False) + "\n")
    write_text(bundle / "install_proposal.md", proposal_markdown(proposal))
    write_intent(bundle, args.program, key, "plan-install", "PROPOSED")

    print("Status\tPROPOSED")
    print("Program_Key\t" + key)
    print("Target_Env\t" + target_env)
    print("Proposal_JSON\t" + rel(bundle / "install_proposal.json"))
    print("Proposal_MD\t" + rel(bundle / "install_proposal.md"))
    if commands:
        print("Command\t" + commands[0])
    else:
        print("Command\tPROPOSAL_ONLY")
    return 0


