def load_proposal(path: Path) -> dict[str, Any]:
    data = json.loads(path.read_text(encoding="utf-8"))
    missing = sorted(REQUIRED_PROPOSAL_FIELDS - set(data))
    if missing:
        raise ValueError("proposal missing required field(s): " + ", ".join(missing))
    if data.get("created_by") != CREATED_BY or data.get("proposal_schema_version") != SCHEMA_VERSION:
        raise ValueError("proposal was not generated by this version of program_onboard.py")
    return data


def resolve_install_proposal_path(path: Path) -> Path:
    resolved = path.expanduser().resolve()
    if is_protected_write_path(resolved):
        raise ValueError("proposal path must not be under a protected data/tools directory (e.g. ~/data or ~/tools)")
    if resolved.name != "install_proposal.json":
        raise ValueError("proposal path must be named install_proposal.json")
    validate_bundle_dir(resolved.parent)
    return resolved


def validate_conda_proposal(proposal: dict[str, Any]) -> list[str]:
    errors: list[str] = []
    if proposal.get("source_type") != "conda":
        errors.append("automatic install only supports source_type=conda")
    if proposal.get("install_method") not in {"conda_create_env", "conda_install_existing_env"}:
        errors.append("install_method is not a supported Conda method")
    target_env = str(proposal.get("target_env", ""))
    if not safe_env_name(target_env):
        errors.append("target_env must be a simple Conda env name, not a path")

    package_name = str(proposal.get("package_name", ""))
    if not validate_package_name(package_name):
        errors.append("package_name must be a simple Conda package token")

    version_spec = str(proposal.get("version_spec", ""))
    if not validate_version_spec(version_spec):
        errors.append("version_spec contains unsafe characters")

    channels = proposal.get("channels")
    if not isinstance(channels, list) or not channels or not all(isinstance(channel, str) for channel in channels):
        errors.append("channels must be a non-empty list of strings")
        channels = []
    else:
        bad_channels = [channel for channel in channels if not validate_channel_name(channel)]
        if bad_channels:
            errors.append("channels contain unsafe token(s): " + ", ".join(bad_channels))

    command_argv = proposal.get("command_argv")
    if not isinstance(command_argv, list) or len(command_argv) != 1 or not isinstance(command_argv[0], list):
        errors.append("proposal must contain exactly one command_argv list")
        return errors

    argv = [str(part) for part in command_argv[0]]
    if not argv or Path(argv[0]).name not in {"mamba", "conda"}:
        errors.append("installer must be mamba or conda")
        installer = ""
    else:
        installer = Path(argv[0]).name
    expected_action = "install" if proposal.get("reuse_existing") else "create"
    if len(argv) < 6 or argv[1] != expected_action:
        errors.append(f"Conda command must use '{expected_action}'")
    if "-n" not in argv:
        errors.append("Conda command must name the target env with -n")
    else:
        env_arg = argv[argv.index("-n") + 1] if argv.index("-n") + 1 < len(argv) else ""
        if env_arg != target_env:
            errors.append("Conda command target env does not match proposal target_env")
    user_tools_prefix = str(Path.home() / "tools")
    if any(user_tools_prefix in part for part in argv):
        errors.append("automatic install command must not write under ~/tools/")
    if any(part in {";", "&&", "||", "|", ">", ">>"} for part in argv):
        errors.append("command_argv contains shell control syntax")

    if installer and not errors:
        expected_argv = build_conda_argv_with_installer(
            installer,
            target_env,
            package_name,
            version_spec,
            channels,
            bool(proposal.get("reuse_existing")),
        )
        if argv != expected_argv:
            errors.append("command_argv does not exactly match proposal fields")
        expected_commands = [shlex.join(expected_argv)]
        if proposal.get("commands") != expected_commands:
            errors.append("commands does not exactly match command_argv")
    return errors


def proposal_summary(proposal: dict[str, Any]) -> str:
    commands = proposal_command_strings(proposal)
    lines = [
        "Install summary:",
        f"- Program key: {proposal['program_key']}",
        f"- Target env: {proposal['target_env']}",
        f"- Source type: {proposal['source_type']}",
        f"- Network required: {proposal['network_required']}",
        "- Expected writes:",
    ]
    for item in proposal.get("expected_writes") or []:
        lines.append(f"  - {item}")
    lines.append("- Command:")
    lines.extend(f"  {command}" for command in commands)
    return "\n".join(lines)


def proposal_command_strings(proposal: dict[str, Any]) -> list[str]:
    command_argv = proposal.get("command_argv")
    if isinstance(command_argv, list) and len(command_argv) == 1 and isinstance(command_argv[0], list):
        return [shlex.join([str(part) for part in command_argv[0]])]
    return [str(command) for command in (proposal.get("commands") or [])]


def install(args: argparse.Namespace) -> int:
    try:
        proposal_path = resolve_install_proposal_path(Path(args.proposal))
    except ValueError as exc:
        print("Status\tBLOCKED")
        print("Reason\t" + str(exc))
        return 2
    try:
        proposal = load_proposal(proposal_path)
    except (OSError, json.JSONDecodeError, ValueError) as exc:
        print("Status\tERROR")
        print("Reason\t" + str(exc))
        return 2

    proposal_evidence_dir = Path(str(proposal.get("evidence_dir", ""))).resolve()
    if proposal_evidence_dir != proposal_path.parent.resolve():
        print("Status\tBLOCKED")
        print("Reason\tproposal evidence_dir must match the proposal directory")
        return 2

    print(proposal_summary(proposal))
    if not args.yes:
        print("Status\tBLOCKED")
        print("Reason\tinstall requires --yes after reviewing the proposal")
        return 2

    errors = validate_conda_proposal(proposal)
    if errors:
        print("Status\tBLOCKED")
        for error in errors:
            print("Reason\t" + error)
        return 2

    target_env = proposal["target_env"]
    env_exists = find_env(target_env)
    if proposal.get("reuse_existing"):
        if not env_exists:
            print("Status\tBLOCKED")
            print("Reason\treuse_existing is true but target env is not present: " + target_env)
            return 2
    elif env_exists:
        print("Status\tBLOCKED")
        print("Reason\tTarget env already exists: " + target_env)
        print("Existing_Env_Prefix\t" + env_exists["prefix"])
        return 2

    argv = [str(part) for part in proposal["command_argv"][0]]
    installer = shutil.which(argv[0])
    if not installer:
        print("Status\tBLOCKED")
        print("Reason\tInstaller not found in PATH: " + argv[0])
        return 2
    argv[0] = installer

    log_path = proposal_path.parent / "install.log"
    log_path.parent.mkdir(parents=True, exist_ok=True)
    with log_path.open("w", encoding="utf-8") as log:
        log.write("# install.log\n")
        log.write("created_at\t" + iso_now() + "\n")
        log.write("command\t" + shlex.join(argv) + "\n\n")
        log.flush()
        proc = subprocess.run(argv, stdout=log, stderr=subprocess.STDOUT, text=True, check=False)
        log.write("\nexit_code\t" + str(proc.returncode) + "\n")

    print("Install_Log\t" + rel(log_path))
    if proc.returncode != 0:
        print("Status\tFAILED")
        print("Exit_Code\t" + str(proc.returncode))
        return proc.returncode
    print("Status\tINSTALLED")
    print("Next\tpython3 scripts/program_onboard.py capture " + shlex.quote(proposal["program_name"]) + " --evidence-dir " + shlex.quote(str(proposal_path.parent)))
    return 0


