def draft_card(args: argparse.Namespace) -> int:
    try:
        project_root = validate_project_root(args.project_root)
        bundle = validate_bundle_dir(resolve_under_project(args.evidence_dir, project_root), project_root, args.allow_external_paths)
    except ValueError as exc:
        print("Status\tBLOCKED")
        print("Reason\t" + str(exc))
        return 2
    if not bundle.exists() or not bundle.is_dir():
        print("Status\tERROR")
        print("Reason\tEvidence dir not found: " + str(bundle))
        return 2
    intent = read_intent(bundle)
    proposal = load_proposal_if_present(bundle)
    key = proposal.get("program_key") or intent.get("Program_Key") or bundle.parent.name
    try:
        output_path = validated_draft_path(
            Path(args.output_card) if args.output_card else CARD_DRAFT_DIR / f"{key}.md",
            args.allow_external_paths,
        )
    except ValueError as exc:
        print("Status\tBLOCKED")
        print("Reason\t" + str(exc))
        return 2
    existed = output_path.exists()
    if existed and not args.force:
        print("Status\tBLOCKED")
        print("Reason\tDraft card already exists; use --force to overwrite")
        print("Draft_Card\t" + rel(output_path))
        return 2
    output_path.parent.mkdir(parents=True, exist_ok=True)
    text = draft_card_text(bundle, output_path)
    write_text(output_path, text)
    write_text(bundle / "card_draft.md", text)
    print("Status\t" + ("OVERWRITTEN" if existed else "DRAFTED"))
    print("Draft_Card\t" + rel(output_path))
    print("Evidence_Copy\t" + rel(bundle / "card_draft.md"))
    print("Next\tReview manually, run a pilot, then move to active cards and update registry.tsv only after confirmation")
    return 0


