def package_spec(package: str, version_spec: str) -> str:
    spec = version_spec.strip()
    if not spec:
        return package
    if spec.startswith(("=", ">", "<", "!")):
        return package + spec
    return f"{package}={spec}"


def validate_package_name(package: str) -> bool:
    return not unsafe_token(package)


def validate_version_spec(version_spec: str) -> bool:
    if not version_spec:
        return True
    return not unsafe_token(version_spec.lstrip("=<>!~"))


def validate_channel_name(channel: str) -> bool:
    return not unsafe_token(channel)


def channel_args(channels: list[str]) -> list[str]:
    argv: list[str] = []
    for channel in channels:
        argv.extend(["-c", channel])
    return argv


def installer_name() -> str:
    if shutil.which("mamba"):
        return "mamba"
    if shutil.which("conda"):
        return "conda"
    return "mamba"


