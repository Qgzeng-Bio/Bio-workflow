#!/usr/bin/env python3
"""Semi-automated onboarding for unknown bioinformatics programs.

The tool is intentionally conservative:

- ``probe`` and ``plan-install`` only write project-local evidence files.
- ``install`` executes only a proposal generated by this script and requires
  ``--yes``.
- Third-version automatic installation supports Conda/Mamba environments only.
  Source, binary, GitHub, and container routes can be proposed but are not
  executed here.
"""

