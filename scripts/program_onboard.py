def plan_install(args: argparse.Namespace) -> int:
    key = program_key(args.program)
    package_name = args.package or key
    target_env = args.target_env or f"bio_{key}"
    source_type = args.source
    channels = args.channel or DEFAULT_CHANNELS
    try:
        project_root = validate_project_root(args.project_root)
        out_root = validated_out_root(args.out_root, project_root)
    except ValueError as exc:
        print("Status\tBLOCKED")
        print("Reason\t" + str(exc))
        return 2
    bundle = ensure_bundle(key, out_root)

    discovery_rows: list[dict[str, str]] = []
    envs, env_rows = conda_envs()
    discovery_rows.extend(env_rows)
    write_tsv(bundle / "local_discovery.tsv", discovery_rows, ["Probe", "Status", "Value", "Details"])

    if source_type == "conda" and not safe_env_name(target_env):
        write_intent(bundle, args.program, key, "plan-install", "BLOCKED", {"Reason": "unsafe target env name"})
        print("Status\tBLOCKED")
